// Callback para boton de copiar
function copyToClipBoard(event) {
  let target =
    event.target.tagName === "BUTTON" ? event.target : event.target.parentNode;
  let btn = target;
  let code = document.getElementById(
    target.getAttribute("clip-toogle").split("#")[1]
  ).textContent;

  navigator.clipboard
    .writeText(code)
    .then(function () {
      btn.innerHTML = iconCopiedSuccess;
    })
    .catch(function (error) {
      btn.innerHTML = iconCopiedFailure;
    });
}

// Listeners para boton copiar
for (let i = 0; i < snipetCard.length; i++) {
  snipetCard[i].addEventListener("click", copyToClipBoard);
}